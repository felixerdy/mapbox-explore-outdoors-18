namespace GoogleARCore.Examples.AugmentedImage
{
    using System;
    using System.Collections.Generic;
    using System.Runtime.InteropServices;
    using GoogleARCore;
    using GoogleARCoreInternal;
    using UnityEngine;

    /// <summary>
    /// 
    /// </summary>
    public class AugmentedMapVisualizer : MonoBehaviour
    {
        /// <summary>
        /// The AugmentedImage to visualize.
        /// </summary>
        public AugmentedImage Image;

        /// <summary>
        /// A map that gets displayed
        /// </summary>
		public GameObject AugmentedMap;

        /// <summary>
        /// The Unity Update method.
        /// </summary>
        public void Update()
        {
            if (Image == null || Image.TrackingState != TrackingState.Tracking)
            {
                AugmentedMap.SetActive(false);

                return;
            }

            float halfWidth = Image.ExtentX / 2;
            float halfHeight = Image.ExtentZ / 2;

			AugmentedMap.transform.localPosition = (halfWidth * Vector3.up) + (halfHeight * Vector3.up);
			AugmentedMap.SetActive (true);
        }
    }
}
